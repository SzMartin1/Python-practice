# if statement is a block of code which will only execute if it's condition is true
age= int(input("How old are you?: "))

if age == 100:
    print("Wow, you're a century old bro!")
elif age >= 18:
    print("Wow, you are an adult!")
elif age < 0:
    print("You haven't been born yet bro!")
else:
    print("Wow, you're a child!")


